    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        String nama = getNama();
        String alamat = getAlamat();
        String contact = getContact();
        if ("pegawai".equals(driver.getUserLevel())) {
            Pegawai pegawai = driver.getPegawaiByEmail(driver.getUserEmail());
            if (!"".equals(nama)) {
                pegawai.setNama(nama);
                driver.getDb().updatePegawai(pegawai.getId(), pegawai);
            }
            if (!"".equals(alamat)) {
                pegawai.setAlamat(alamat);
                driver.getDb().updatePegawai(pegawai.getId(), pegawai);
            }
            if (!"".equals(contact)) {
                pegawai.setContact(contact);
                driver.getDb().updatePegawai(pegawai.getId(), pegawai);
            }
        } else if ("supplier".equals(driver.getUserLevel())) {
            Supplier supplier =  driver.getSupplierByEmail(driver.getUserEmail());
            if (!"".equals(nama)) {
                supplier.setNama(nama);
                driver.getDb().updateSupplier(supplier.getId(), supplier);
            }
            if (!"".equals(alamat)) {
                supplier.setAlamat(alamat);
                driver.getDb().updateSupplier(supplier.getId(), supplier);
            }
            if (!"".equals(contact)) {
                supplier.setContact(contact);
                driver.getDb().updateSupplier(supplier.getId(), supplier);
            }
        } else if ("penyewa".equals(driver.getUserLevel())) {
            Penyewa penyewa = driver.getPenyewaByEmail(driver.getUserEmail());
            if (!"".equals(nama)) {
                penyewa.setNama(nama);
                driver.getDb().updatePenyewa(penyewa.getId(), penyewa);
            }
            if (!"".equals(alamat)) {
                penyewa.setAlamat(alamat);
                driver.getDb().updatePenyewa(penyewa.getId(), penyewa);
            }
            if (!"".equals(contact)) {
                penyewa.setContact(contact);
                driver.getDb().updatePenyewa(penyewa.getId(), penyewa);
            }
        }
        this.dispose();
        this.setVisible(false);
    }//GEN-LAST:event_submitButtonActionPerformed

