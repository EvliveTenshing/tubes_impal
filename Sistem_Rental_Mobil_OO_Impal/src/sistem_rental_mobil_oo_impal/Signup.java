    private boolean validateEmail() {
        Pattern p = Pattern.compile("[a-zA-Z0-9][a-zA-Z0-9. ]*@[a-zA-Z0-9]+([.][a-zA-Z]+)+");
        Matcher m = p.matcher(getEmail());
        if (m.find() && m.group().equals(getEmail())) {
            return true;
        } else {
            return false;
        }
    }
    
