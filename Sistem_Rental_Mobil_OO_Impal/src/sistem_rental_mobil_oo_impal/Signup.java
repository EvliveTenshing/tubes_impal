    private void signUpButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpButtonActionPerformed
        setButtonGroup();
        String as = signUpSebagai.getSelection().getActionCommand();
        String email = getEmail();
        boolean emailAdaDiDatabase = false;
        ArrayList<Supplier> listSupplier = driver.getListSupplier();
        ArrayList<Penyewa> listPenyewa = driver.getListPenyewa();
        ArrayList<Pegawai> listPegawai = driver.getListPegawai();
        if (validateEmail() == true) {
            for (Supplier p : listSupplier) {
                if (p.getEmail().equals(email)) {
                    emailAdaDiDatabase = true;
                }
            }
            for (Penyewa p : listPenyewa) {
                if (p.getEmail().equals(email)) {
                    emailAdaDiDatabase = true;
                }                
            }
            for (Pegawai p : listPegawai) {
                if (p.getEmail().equals(email)) {
                    emailAdaDiDatabase = true;
                }                
            }          
            if (emailAdaDiDatabase == false) {
                if ("supplier".equals(as)) {
                    if (("".equals(getPassword())) || ("".equals(getNama())) || ("".equals(getAlamat())) || ("".equals(getContact()))) {
                        JOptionPane.showMessageDialog(null, "Formulir yang anda isi tidak lengkap");
                    }
                    else {
                        Supplier supplier = new Supplier(getEmail(), getPassword(), getNama(), getAlamat(), getContact(), "Supplier");
                        driver.getDb().insertSupplier(supplier);
                        reset();
                        this.dispose();
                        Login login = new Login();
                        login.setVisible(true);
                    }
                } else {
                    if (("".equals(getPassword())) || ("".equals(getNama())) || ("".equals(getAlamat())) || ("".equals(getContact()))) {
                        JOptionPane.showMessageDialog(null, "Formulir yang anda isi tidak lengkap");
                    }
                    else {
                        Penyewa penyewa = new Penyewa(getEmail(), getPassword(), getNama(), getAlamat(), getContact(), "Penyewa");
                        driver.getDb().insertPenyewa(penyewa);
                        reset();
                        this.dispose();
                        Login login = new Login();
                        login.setVisible(true);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Email telah terdaftar");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Email tidak valid mohon input kembali");
        }
    }//GEN-LAST:event_signUpButtonActionPerformed

